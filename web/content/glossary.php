									<p></p>
									<b>VARIABLE ANNUITY:</b>&nbsp; A form of annuity policy under which the amount of each benefit payment is not guaranteed or specified in the policy, but which instead fluctuates according to the earnings of a separate account fund.
									<p></p>
									<b>VARIABLE LIFE INSURANCE:</b>&nbsp;A form of life insurance whose face value fluctuates depending upon the value of the dollar, securities or other equity products supporting the policy at the time payment is due.
									<p></p>
									<b>VIN (VEHICLE IDENTIFICATION NUMBER):</b>&nbsp; A 17-digit alpha-numeric code that provides valuable information concerning the vehicle&rsquo;s serial number, make, model, options, and year in official records (like a Social Security number for your car).
									<p></p>
									<b>VIATOR:</b>&nbsp; A terminally with a life-threatening or terminal illness who sells his or her life insurance policy.
									<p></p>
									
