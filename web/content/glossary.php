									<a name=l>
									<div class=glossarytitle> &nbsp;<b>L</b><a href=#home class=backtotop >back to top</a></div>
									<p></p>
									<b>LAPSE:</b> The termination or discontinuance of a policy due to non-payment of a premium. 
									<p></p>
									<b>LIABILITY:</b>&nbsp; Responsibility to another for one&rsquo;s negligence that results in injury or damage.
									<p></p>
									<b>LIABILITY INSURANCE:</b>&nbsp; An auto insurance coverage that pays for injuries to the other party and damages to the other vehicle resulting from an accident the policyholder caused.&nbsp; It also pays if the accident was caused by someone covered by the policyholder&rsquo;s policy, including a driver operating the car with their permission.
									<p></p>
									<b>LIABILITY COVERAGE:</b>&nbsp; Covers losses for which an insured is legally liable.&nbsp; For homeowners insurance, for example, liability coverage protects the policyholder against financial loss if they are sued and found legally responsible for someone else&rsquo;s injury or property damage.
									<p></p>
									<b>LICENSE &ndash; AGENT OR BROKER:</b> Certification, issued by the department of insurance, that an individual is qualified to solicit insurance applications for the period covered. 
									<p></p>
									<b>LICENSE &ndash; COMPANY:</b> Certification, issued by the department of insurance, stating that an insurance company is qualified to do business in the state.
									<p></p>
									<b>LIFE EXPECTANCY:</b>&nbsp; The average number of years a person is expected to live, based on a national average per age group and other factors. 
									<p></p>
									<b>LIFE INSURANCE:</b>&nbsp; Insurance coverage that pays out a set amount of money to specified beneficiaries upon the death of the individual who is insured.
									<p></p>
									<b>LIMIT OF LIABILITY:</b>&nbsp; The most an insurance company agrees to pay in the case of a loss.
									<p></p>
									<b>LOSS:</b>&nbsp; The amount an insurance company pays for damages under the terms of the policy.
									<p></p>
									<b>LOSS OF USE:</b>&nbsp; A provision in homeowners and renters insurance policies that reimburses policyholders for the additional costs (housing, food, and other essentials) of having to live elsewhere while the home is being restored following a disaster.
									<p></p>
									<b>LOSS PAYEE/LIEN HOLDER:</b>&nbsp; A person or entity with a legally secured insurable interest in another&rsquo;s property, usually a financial institution that loaned money to buy a car.&nbsp; The car is the loan collateral.&nbsp; If the auto is damaged in an accident, loss payments will be made to you and to the loss payee on your policy.
									<p></p>
									<b>LOSS PAYABLE CLAUSE:</b>&nbsp; An insurance clause that authorizes loss payments to a person or entity having an insurable interest in the covered property.
									<p></p>
									<b>LOSS RATIO:</b> Percentage of losses incurred against earned premiums for a given period.
									<p></p>
									
									
									<a name=m>
									<div class=glossarytitle> &nbsp;<b>M</b><a href=#home class=backtotop >back to top</a></div>
									<p></p>
									<b>MEDICAL PAYMENTS:</b> Optional coverage under an auto policy to pay for medical expenses for bodily injury caused by an auto accident, regardless of fault.&nbsp; Coverage for persons other than the named insured and his or her family members is typically restricted to circumstances occurring while they are occupants of the insured auto.
									<p></p>
									<b>MINIMUM PREMIUM:&nbsp;</b> The lowest amount of premium to be charged for providing a particular insurance coverage. 
									<p></p>
									<b>MORTGAGE CLAUSE:</b>&nbsp; Property insurance provisions granting protection for the mortgagee named in the policy.&nbsp; It establishes that loss to mortgaged property is payable to the insured and to the mortgagee named in the policy.
									<p></p>
									<b>MULTI-CAR DISCOUNT:</b>&nbsp; A discount offered by some insurance companies for those with more than one vehicle insured on the same policy.
									<p></p>
									<b>MVR &ndash; MOTOR VEHICLE RECORD:</b>&nbsp; A motor vehicle record contains information obtained from an individual&rsquo;s driver license application, abstracts of convictions and accidents.
									<p></p>
									<b>MORTGAGE INSURANCE: </b>One of the basic uses of life insurance. Family heads buy mortgage insurance for the specific purpose of paying off any mortgage balance outstanding at their death. 
									<p></p>
									<b>MULTIPLE LINE:</b> A general term referring to fire and casualty insurance in general. A multiple line company writes auto, fire, health, commercial, boatowners, homeowners, individual fire, theft insurance.
									<p></p>
									<b>MUTUAL INSURANCE COMPANIES:</b> Insurance companies without capital stock, owned by the policyholders for the purpose of sharing in the profits through dividends at the end of the policy year.
									<p></p>
									
									
									<a name=n>
									<div class=glossarytitle> &nbsp;<b>N</b><a href=#home class=backtotop >back to top</a></div>
									<p></p><b>NAMED DRIVER EXCLUSION:</b>&nbsp; An endorsement to an auto policy that provides that apolicy does not cover accidents when a specifically named person is the driver.
									<p></p>
									<b>NAMED INSURED:</b>&nbsp; Any person, firm or corporation designated by name as the insured person(s) in a policy.&nbsp; Others may be protected by policy definition even though their names aren&rsquo;t on the policy, such as other drivers operating (with consent) the named insured&rsquo;s covered auto.
									<p></p>
									<b>NAMED PERILS COVERAGE:</b>&nbsp; A property insurance term referring to exact causes of loss specifically listed as covered.
									<p></p>
									<b>NEGLIGENCE:</b> The failure to use the care that a reasonable and prudent person would have used under the same or similar circumstances.
									<p></p>
									<b>NON-OWNED AUTOMOBILE:</b>&nbsp; In commercial auto policies, coverage for autos that are used in connection with the named insured&rsquo;s business but are neither owned, leased, hired, rented or borrowed by the named insured.&nbsp; The term specifically applies to vehicles owned by employees and used for company business.
									<p></p>
									<b>NON-RENEWAL:</b>&nbsp; a decision by an insurance company not to renew a policy.
									<p></p>
									NONSTANDARD AUTO (HIGH RISK AUTO OR SUBSTANDARD AUTO):&nbsp; Insurance for motorists who have poor driving records or have been canceled or refused insurance.&nbsp; The premium is much higher than standard auto due to the additional risks.
									<p></p>
									
									
									<a name=o>
									<div class=glossarytitle> &nbsp;<b>O</b><a href=#home class=backtotop >back to top</a></div>
									<p></p><b>OCCUPANCY:</b> This refers to the use of property. A home, for example, may have a real estate office in it. This dwelling would then have a "business occupancy." Occupancy plays a very important part in computing rates and determining the acceptance or rejection of risks. 
									<p></p>
									<b>OCCUPATIONAL HAZARD:</b> The danger of suffering a sickness or injury due to the hazards of an occupation.
									<p></p>
									<b>OCCURRENCE:</b>&nbsp; An event that results in an insured loss.&nbsp; In some lines of business, such as liability, an occurrence is distinguished from an accident in that the loss doesn&rsquo;t have to be sudden and fortuitous and can result from continuous or repeated exposure which results in bodily injury or property damage neither expected nor intended by the insured. 
									<p></p>
									<b>OCCURRENCE COVERAGE:</b> Occurrence coverage is insurance for incidents of liability alleged to have occurred during the term of the policy, no matter when the claim is reported.
									<p></p>
									<b>OMNIBUS CLAUSE:</b> An automobile policy provision which covers persons driving the named insured&rsquo;s auto with the named insured&rsquo;s permission.
									<p></p>
									<b>ORIGINAL AGE:</b>&nbsp; The age you were when you bought an insurance&nbsp; policy.
									<p></p>
									<b>OWNER:</b> The person who can legally exercise all rights and privileges in the life policy. This will usually be the insured, but may be any other party to whom proper transfer of these rights and privileges has been made.
									<p></p>
									
									
									<a name=p>
									<div class=glossarytitle> &nbsp;<b>P</b><a href=#home class=backtotop >back to top</a></div>
									<p></p>
									<b>PAID-UP:</b>&nbsp; This event occurs when a life insurance policy will not require any further premiums to keep the coverage in force.
									<p></p>
									<b>PAID-UP ADDITIONS:</b>&nbsp; Additional amounts of life insurance purchased using dividends; these insurance amounts require no further premium payments.
									<p></p>
									<b>PARTIAL DISABILITY:</b> An injury which prevents the insured from performing one or more, but not all, important duties of his job.
									<p></p>
									<b>PERIL:</b> The cause of possible loss, such as fire, windstorm, theft, explosion, or riot.
									<p></p>
									<b>PERSISTENCY:</b> A term used to refer to the length of time insurance remains continuously in force.
									<p></p>
									<b>PERSONAL AUTO POLICY (PAP):</b>&nbsp; A policy insuring private-passenger autos owned by individuals.
									<p></p>
									<b>PERSONAL INJURY:</b>&nbsp; A General Liability coverage for insurable offenses that cause harm, other than bodily injury, such as false arrest, detention or imprisonment, malicious prosecution, wrongful eviction, slander, libel and invasion of privacy. 
									<p></p>
									<b>PERSONAL INJURY PROTECTION (PIP):</b>&nbsp; An automobile insurance coverage mandated by law in some states.&nbsp; The statues typically require insurers to provide or offerto provide first-party benefits for medical expenses, loss of income, funeral expenses and <b>similar expenses without regard to fault.</b>
									<p></p>
									<b>PERSONAL PROPERTY:</b> This type of property is usually movable and easily transportable. On the other hand, real property generally is considered to be immovable such as land and things affixed to it. A rule of thumb definition for personal property is "everything other than real property." 
									<p></p>
									<b>PHYSICAL HAZARD:</b> This refers to the material, structural or operational features of the risk itself, apart from the persons owning or managing it. Electrical wiring, building construction, type of heating system, are examples of physical hazards.
									<p></p>
									<b>POLICIES-IN-FORCE:</b> Policies written and recorded on the books of the carrier which are unexpired as of a given date.
									<p></p>
									<b>POLICY:</b> The name generally used to mean the written contract of insurance.
									<p></p>
									<b>POLICY FEE:</b> An amount charged by some companies in addition to the first regular premium. Also called "joiner&rsquo;s fee." 
									<p></p>
									<b>POLICYHOLDER:</b> One who owns an insurance policy. A mortgagee often is issued a copy of an insurance policy, or a certificate of insurance, at the request of the insured, but he is not a policyholder.
									<p></p>
									<b>POLICY LOAN (LIFE):</b> A loan made by an insurance company to a policyholder on the security of the cash value of his policy.
									<p></p>
									<b>POLICY PERIOD:</b>&nbsp; The period a policy is in force, from the beginning or effective date to the expiration date.
									<p></p>
									<b>PREFERRED RISK:</b>&nbsp; A positive characteristic of someone seeking to be insured.&nbsp; Usually means a better likelihood for long life, and usually means a lower premium. 
									<p></p>
									<b>PREMISES:</b>&nbsp; The location where coverage applies.
									<p></p>
									<b>PRE-EXISTING CONDITION:</b> A physical condition which existed prior to the issuance of a health policy.
									<p></p>
									<b>PREMIUM:</b>&nbsp; The amount paid by an insured to an insurance company to obtain or <b>maintain an insurance policy.</b>
									<p></p>
									<b>PREMIUMS-IN-FORCE:</b> Premium dollars which have been written and are unexpired on the books of the insurance carrier.
									<p></p>
									<b>PREMIUM EARNED:</b>&nbsp; The amount of the premium, paid for in advance, that has been&ldquo; earned&rdquo; by virtue of the fact that time has passed without a claim.&nbsp; 
									<p></p>
									<b>PREMIUM UNEARNED:</b> The portion of an insurance premium which applies to the unexpired portion of the policy period.&nbsp;
									<p></p>
									<b>PRIMARY BENEFICIARY:</b>&nbsp; In life insurance, the beneficiary designated by the insured as the first to receive policy benefits.
									<p></p>
									<b>PRIMARY POLICY:</b>&nbsp; The insurance policy that pays first when you have a loss that is covered by more than one policy.
									<p></p>
									<b>PROCEEDS:</b> The net amount of money payable by the company at the death of an insured or at the maturity of a policy.
									<p></p>
									<b>PROPERTY DAMAGE COVERAGE:</b> An agreement by an insurance carrier to protect an insured against legal liability for damage by his automobile to the property of another.
									<p></p>
									<b>PROPERTY DAMAGE:</b>&nbsp; In the general liability policy, a physical injury to property, resulting in the loss of use.
									<p></p>
									<b>PROPERTY INSURANCE:</b>&nbsp; First-party insurance for real and personal property against physical loss or damage.
									<p></p>
									<b>PRO RATA CANCELLATION:</b>&nbsp; The cancellation of an insurance policy with the return premium being the full proportion of premium for the unexpired term of the policy, without penalty for early cancellation.
									<p></p>
									<b>PROVISIONS:</b>&nbsp; Details of an insurance policy which explain the benefits, conditions and other features of the insurance contract.
									<p></p>
									<b>PROXIMATE CAUSE:</b> The dominating cause of loss or damage; an unbroken chain of events between the occurrence of an insured peril and damage to property. As an illustration, water damage occurring from fire fighting activities is covered under the fire policy because fire was the proximate cause of the loss. 
									<p></p>
									
									
