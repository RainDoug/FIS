									<a name=a>
									<div class=glossarytitle> &nbsp;<b>A</b><a href=#home class=backtotop >back to top</a></div>
									<p></p>
									<b>ACCIDENT:</b> An event causing loss, which occurs without being expected or designed, usually specific in time and place.
									<p></p>
									<b>ACCELERATED DEATH BENEFITS:</b> A provision that will pay all or part of the policy death benefits while the policyholder is still alive.&nbsp; Conditions include proof that the policyholder is terminally ill with a life expectancy of less than 12 months, has a specified life-threatening disease or is in a long-term care facility such as a nursing home.&nbsp; By accepting an accelerated benefit payment, a person could be ruled ineligible for Medicaid or other government benefits.&nbsp; The proceeds may also be taxable.&nbsp; 
									<p></p>
									<b>ACCIDENTAL DEATH BENEFIT:</b> Provision for payment of an additional amount&ndash; usually equal to the face amount of insurance  - if the insured is killed in an accident. Popularly known as double indemnity. 
									<p></p>
									<b>ACQUISITION COSTS:</b> Expenses incurred in acquiring new business premiums and conservation of renewal business. Broad in scope, it includes cost of soliciting business, issuance of policies, collection of premiums, agents&rsquo; compensation, field supervision, advertising, and any other expense reasonably attributable to acquisition and conservation of written premiums. 
									<p></p>
									<b>ACTUAL CASH VALUE (ACV):</b> The cost of replacing or restoring property at prices prevailing at the time and place of the loss, less depreciation, however caused. Another definition: the sum of money required to replace property less depreciation, which includes physical wear and tear, and obsolescence.
									<p></p>
									<b>ACTUARY:</b> A person whose principal function is to make the technical calculations required for the pricing of insurance policies.
									<p></p>
									<b>ADDITIONAL EXTENDED COVERAGE:</b> A second endorsement on the fire policy(fire and lightning with extended coverage) which insures the dwelling and/or contents against water damage from plumbing, etc.; boiler explosion; glass breakage; and damage by ice and snow, freezing, fall of trees, collapse, vandalism, vehicles owned by insured or tenants and landslide.
									<p></p>
									<b>ADDITIONAL INSURED:</b> One who is protected by an insurance policy other than the named insured. Examples: In automobile insurance, one who drives the insured&rsquo;s car with his consent ordinarily is protected. In property insurance, this might be a co-owner, mortgagee, or lien holder. 
									<p></p>
									<b>ADJUSTER:</b> A person who investigates and settles losses for an insurance carrier or the insured.
									<p></p>
									<b>ADVANCE PREMIUM:</b> Most companies give the insured the right of making premium payments in advance. 
									<p></p>
									<b>AGE CHANGE:</b> An age change occurs on the date, halfway between birth dates, on which the life insurance age changes. Immediately after, the premium for new life insurance will be computed to the age on the next following birth date. The life insurance age is the age at nearest birthday.
									<p></p>
									<b>AGE LIMITS:</b> The ages below and above which the company will not accept applicants. 
									<p></p>
									<b>AGENCY:</b> An organization which solicits insurance for one or more carriers and may perform other functions such as issuing policies and adjusting losses. 
									<p></p>
									<b>AGENT:</b>1.&nbsp;An individual who solicits insurance for one or more carriers and may perform other functions, such as issuing policies. 2.&nbsp;Agents of a direct writer are sales employees of one company only. 
									<p></p>
									<b>AGE OF CAR (age group):</b> A term used to classify cars according to age for rating purposes. 
									<p></p>
									<b>ALL PHYSICAL LOSS FORM:</b> This coverage protects against loss from "all risks of physical loss" for dwellings subject to certain exclusions contained in the form.
									<p></p>
									<b>ANNUAL POLICY:</b> Insurance policy written for a term of one year.
									<p></p>
									<b>ANNUITY:</b>&nbsp; A contract in which the buyer deposits money with a life insurance company for investment.&nbsp; The contract provides for specific payments to be made at regular intervals for a fixed period or for life.
									<p></p>
									<b>ANNUITANT:</b> The person during whose life an annuity is payable, usually the person to receive the annuity. 
									<p></p>
									<b>APPLICATION:</b> A request to a company for a policy. The application is a conditional offer to buy. If the medical examination and the inspection are in order, the company usually will accept the offer. It may be the policy named in the application or, if the applicant is substandard, it may be on a higher premium or other policy form.
									<p></p>
									<b>APPRAISAL:</b> Determination of the value of property or the extent of damage by impartial experts. Many property insurance policies provide for "appraisals" where the company and the insured cannot agree on the amount or the extent of a loss. 
									<p></p>
									<b>APPROVED:</b> In fire insurance, usually means that the construction, equipment, preventive and protective devices meet established requirements for insurance. In many cases,"approved" construction results in reduced insurance premiums.
									<p></p>
									<b>AREA:</b> A territorial subdivision, usually called "rating territory," within a given state used for rating purposes. 
									<p></p>
									<b>ARSON:</b> The willful and malicious burning of property, sometimes with the intent of defrauding an insurance company.
									<p></p>
									<b>ASSETS:</b> All of the property owned by a carrier.&nbsp; 
									<p></p>
									<b>ASSIGNEE:</b> One to whom the legal ownership of a policy or a limited interest therein is transferred.
									<p></p>
									<b>ASSIGNMENT:</b> The partial or complete transfer by a person of his right or interest in a policy to another person. The ability of a person to so assign the policy may be limited by law or individual circumstances. An assignment must be written, signed by the owner-policyholder whose interest is being transferred, properly attested, and the original or a certified copy must be filed with the insuring company. A valid assignment so filed is binding on the company. 
									<p></p>
									<b>ASSURANCE-INSURANCE:</b> These terms are today generally accepted as synonymous, although not originally so. The term "assurance" is used more commonly in Canada and Great Britain than in the United States.
									
									
									<a name=b>
									<div class=glossarytitle> &nbsp;<b>B</b><a href=#home class=backtotop >back to top</a></div>
									<p></p>
									<b>BENEFICIARY (LIFE):</b> The person named in the policy, to whom the insurance money is paid at the death of the insured. 
									<p></p>
									<b>BINDER:</b> A written or oral contract issued temporarily to place insurance in force when it is not possible to issue a new policy or endorse the existing policy immediately. A binder is subject to the premium and all the terms of the policy to be issued.
									<p></p>
									<b>BODILY INJURY BENEFIT COVERAGE:</b> This automobile coverage is designed to protect the insured and any passengers in this car against loss by reason of bodily injury or death caused by the owner or operator of an uninsured automobile (or a "hit-and-run"). Also called uninsured motorist coverage.
									<p></p>
									<b>BODILY INJURY COVERAGE:</b> This coverage, often called "public liability insurance&quot;, protects an insured against legal liability for injury to the person of another arising from an accident.
									<p></p>
									<b>BROAD FORM:</b> A policy affording more liberal benefits, or in fire insurance, an endorsement that grants broader or additional coverages to a basic policy; usually added to a standard fire and extended coverage policy. For example, on a dwelling policy, it usually adds the following: vandalism, glass breakage, falling trees, weight of ice, snow or sleet, collapse. If added to a commercial fire policy, it might include vandalism, falling objects, weight of ice, snow or sleet, and collapse. 
									<p></p>
									<b>BROKER: </b>A representative of the insured in placing insurance with companies. He is paid a commission by the company or its agent. Often a broker also is a licensed "agent" for one or more companies.
									<p></p>
									<b>BUILDER&rsquo;S RISK INSURANCE:</b> Insurance against loss resulting from damage to buildings and to materials incidental to construction, including machinery and equipment, while the buildings are under construction.
									<p></p>
									<b>BURGLARY:</b> Breaking and entering into the premises of another for the purpose of stealing with visible signs of forced entry. 
									<p></p>
									<b>BUSINESS INSURANCE (LIFE):</b> Insurance concerned primarily with the protection of aninsured&rsquo;s business or vocation. Business insurance protects a business against the loss of its valuable lives or key men; stabilizes the business through the establishment of better credit relations; and provides a practical plan for the retirement of business interest in the event of the death of one of the owners. 
									
									
									<a name=c>
									<div class=glossarytitle> &nbsp;<b>C</b><a href=#home class=backtotop >back to top</a></div>
									<p></p>
									<b>CANCELLABLE POLICY:</b> A policy which may be canceled by the company at any time by giving advance notice to the insured and refunding any unearned premium. 
									<p></p>
									<b>CANCELLATION:</b> The discontinuance of an insurance policy before its normal expiration date.
									<p></p>
									<b>CAPITAL SUM:</b> A term in accident insurance to describe the amount payable for death or for loss of hands, feet or sight. Also called principal sum. 
									<p></p>
									<b>CARRIER:</b> The insurance company or the one who agrees to pay the losses. The carrier may be organized as a company, either stock, mutual, or reciprocal, or as an association of underwriters such as Lloyds.
									<p></p>
									<b>CASH SURRENDER VALUE:</b> The amount available in cash upon surrender of a life insurance policy before it matures as a death claim or otherwise. 
									<p></p>
									<b>CASUALTY INSURANCE:</b> A general class of insurance covering liability resulting from accidents and some types of property insurance. It includes among other coverages:automobile, workers compensation, employers liability, general liability, plate glass, theft and personal liability. It excludes life, fire and marine insurance, but, as ordinarily used, includes health insurance and fidelity and surety bonds.
									<p></p>
									<b>CATASTROPHE REINSURANCE: </b>This is a form of insurance written on an excess of loss basis in order to improve the spread of risk against unknown concentrations of liability subject to one occurrence. A deductible is chosen at the amount necessary to reduce the probable frequency of loss to an acceptable level to the reinsurer, and severity of loss to alevel acceptable to the reinsured company.
									<p></p>
									<b>CHARTERED LIFE UNDERWRITER (C.L.U.):</b> A designation conferred in recognition of the attainment of certain standards of education and proficiency in the art and science oflife insurance underwriting. 
									<p></p>
									<b>CHARTERED PROPERTY AND CASUALTY UNDERWRITER (C.P.C.U.):</b> A designation conferred in recognition of the attainment of certain standards of education and proficiency in the art and science of fire and casualty insurance underwriting.
									<p></p>
									<b>CLAIM:</b> A request for payment of a loss which may come under the terms of an insurance contract. There are two types of claims. First party claim is one made by the policyholder in which the policyholder reports the claim directly to his company. A third party claim is one in which a person makes a claim against a policyholder of another company and payment, if any, is made by that company. 
									<p></p>
									<b>CLAIMANT:</b> One who makes a claim.
									<p></p>
									<b>CLAIM SEVERITY:</b> The average cost per claim considering all claims under a certain coverage for a specified period.
									<p></p>
									<b>CLAIMS-MADE COVERAGE:</b> Claims-made insurance is a type of liability protection that covers current legal obligations result for acts of the insured. It provides coverage to the insured for claims reported during the term of the policy. 
									<p></p>
									<b>COINSURANCE PROVISION:</b> An insurance provision for property coverage in which the policy holder must carry an amount of insurance that is at least equal to a set percentage of the value of the property in order to receive full payment of a loss.
									<p></p>
									<b>COLLISION COVERAGE:&nbsp; </b>Covers loss to the insured person&rsquo;s auto caused by its collision with another vehicle or object.
									<p></p>
									<b>COMBINED SINGLE LIMIT:</b>&nbsp; Bodily Injury and Property Damage coverage expressed as one single amount of coverage.
									<p></p>
									<b>COMMERCIAL FIRE:</b> This coverage insures all property not occupied as a residence, excluding farming and manufacturing, against loss by fire. 
									<p></p>
									<b>COMMON DISASTER CLAUSE:</b> In life insurance, this clause is designed to alleviate the hardship that can result if the insured and primary beneficiary die at the same time or within a short period of time of each other. Usually the clause provides that if the primary beneficiary dies either before proof of the insured&rsquo;s death is submitted to the company, or within a stated period (usually 14 or 30 days after the insured&rsquo;s death), the proceeds will be paid to the contingent beneficiary. 
									<p></p>
									<b>COMPREHENSIVE COVERAGE (physical damage other than collision):</b>&nbsp; Pays for damage to or loss of your automobile from causes other than accidents.&nbsp; These include hail, vandalism, flood, fire, and theft.
									<p></p>
									<b>COMPULSORY INSURANCE:</b> The purpose of compulsory insurance laws is to require all residents to buy liability insurance before auto license plates are issued. The law requires proof of financial responsibility (insurance) when license plates are issued.
									<p></p>
									<b>CONCEALMENT:</b> The withholding of material facts from an insurer, either in applying for a policy or making a claim.
									<p></p>
									<b>CONTENTS:</b> A term used to refer to the personal property contained in a building. It maybe household furniture, personal effects, or other types of personal property, movable in nature and not firmly affixed to the real property. 
									<p></p>
									<b>CONTINGENT BENEFICIARY:</b> A beneficiary whose right to receive depends upon the occurrence of a certain contingency &ndash; for example, the right to receive certain benefits only in the event that another named beneficiary dies prior to the time of payment.<b></b>
									<p></p>
									<b>CONTRACT:</b> In most cases, an insurance policy.&nbsp; A policy is considered to be a contract between the insurance company and the policyholder. 
									<p></p>
									<b>CONTRIBUTORY NEGLIGENCE:</b> Carelessness of the injured person that helped to cause the accident in which he was injured. 
									<p></p>
									<b>COVERAGE:</b>&nbsp; Another term for insurance.&nbsp; Can be used to mean either the dollar amounts of insurance purchased (ex: $500,000 of liability coverage), or the type of loss covered(coverage for theft). 
									<p></p>
									
									
									<a name=d>
									<div class=glossarytitle> &nbsp;<b>D</b><a href=#home class=backtotop >back to top</a></div>
									<p></p>
									<b>DAMAGES:</b> In a technical sense, damages refer to the money or compensation recoverable in a lawsuit by a party who has been injured in person or property or rights by the negligence of another.
									<p></p>
									<b>DEATH BENEFIT:</b> Amount paid to the beneficiary upon the death of the insured.
									<p></p>
									<b>DEATH CLAIM:</b> When a policy holder dies, the person entitled to the proceeds must complete certain forms giving due proof of the death and establishing the claimant&rsquo;s right to such proceeds. When filed with the company, the company is said to have received a death claim. 
									<p></p>
									<b>DECLARATIONS PAGE:</b>&nbsp; The page in a policy that shows the name and address of the insurer, the period of time a policy is in force, the amount of the premium, and the amount of coverage.
									<p></p>
									<b>DECREASING TERM LIFE INSURANCE:</b> Term insurance on which the face value slowly decreases in scheduled steps from the date the policy comes into force to the date the policy expires, while the premium remains level.&nbsp; The intervals between decreases are usually monthly or annually.
									<p></p>
									<b>DEDUCTIBLE:</b> The amount an insured person must pay before the insurance company pays the remainder of each covered loss, up to the policy limits. 
									<p></p>
									<b>DEFERRED ANNUITY:</b> An annuity under which the annuity payment period is scheduled to begin at some future date.
									<p></p>
									<b>DEPRECIATION:</b> A decrease in the value of property over a period of time due to wear and tear or obsolescence. Depreciation is used to determine the actual cash value of property at time of loss. (See ACTUAL CASH VALUE.) 
									<p></p>
									<b>DIRECT LOSS:</b> A loss where an insured peril is the proximate cause. If a windstorm blows the roof off a home, the windstorm is the insured peril causing the direct loss or damage. 
									<p></p>
									<b>DISABILITY CLAUSE:</b> A benefit provision forming part of a life insurance policy providing for certain benefits in the event of total and permanent disability from accident or sickness.
									<p></p>
									<b>DIVIDENDS:</b> A return to the policyholder of excess premium over losses and expenses at the end of the policy period. Dividends are authorized by the board of directors, and are payable to all participating policyholders of a specified class.
									<p></p>
									<b>DOMESTIC CARRIER:</b> An insurance company organized in a given state is referred to in that state as a domestic carrier.
									<p></p>
									<b>DOUBLE INDEMNITY:</b>&nbsp; Payment of twice the basic benefit in the event of loss resulting from specified causes or under specified circumstances.
									<p></p>
									
									
									<a name=e>
									<div class=glossarytitle> &nbsp;<b>E</b><a href=#home class=backtotop >back to top</a></div>
									<p></p>
									<b>EARNED PREMIUM:</b> The portion of the total policy premium that has been &ldquo;used up&rdquo; during a policy term.&nbsp; With a one-year policy, half of the total premium has been earned after six months.&nbsp;
									<p></p>
									<b>EFFECTIVE DATE:</b>&nbsp; The date on which an insurance binder or policy goes into effect.
									<p></p>
									<b>ENDORSEMENT:</b> A written agreement attached to a policy expanding or limiting the benefits otherwise payable under the policy.&nbsp; Also called a &ldquo;rider.&rdquo;
									<p></p>
									<b>EQUIPMENT FLOATER:</b>&nbsp; A property insurance coverage for equipment that is ofte nmoved from place to place.
									<p></p>
									<b>ESTIMATED PREMIUM:&nbsp;</b> A preliminary premium amount that could be adjusted based on a variance in exposures.
									<p></p>
									<b>EVIDENCE OF INSURABILITY:&nbsp;</b> To qualify for a particular policy at a particular price, companies have the right to ask for information about health and lifestyle.&nbsp; An insurance company will use this information &ndash; the evidence of insurability &ndash; in deciding if your application for insurance is acceptable and at what premium rate.
									<p></p>
									<b>EXCESS AND SURPLUS LINES INSURANCE:</b>&nbsp; Coverage that is provided by insurers not licensed in states where the risk is located.
									<p></p>
									<b>EXCESS LIABILITY POLICY:</b>&nbsp; A policy that provides additional limits in excess of an underlying liability policy.
									<p></p>
									<b>EXCLUSIONS:</b>&nbsp; Items or conditions that are not covered by the general insurance contract.
									<p></p>
									<b>EXPENSE RATIO:</b> The ratio of a company&rsquo;s operating expenses to premiums. 
									<p></p>
									<b>EXPIRATION DATE:</b> The specified date and time at which the policy terminates.
									<p></p>
									<b>EXPOSURE:</b> Measure of vulnerability to loss, usually expressed in dollars or units.
									<p></p>
									<b>EXTENDED COVERAGE:</b> An extension of the fire policy to cover the additional perils of windstorm, hail, explosion, or riot, attending a strike, civil commotion, aircraft, vehicle and smoke.
									<p></p>
									<b>EXTENDED TERM INSURANCE OPTION:</b>&nbsp; A policy provision that provides the option of continuing the existing amount of insurance as term insurance for as long a period of time as the contract&rsquo;s cash value will purchase.
									<p></p>
									
									
									<a name=f>
									<div class=glossarytitle> &nbsp;<b>F</b><a href=#home class=backtotop >back to top</a></div>
									<p></p>
									<b>FACE AMOUNT:</b> The amount covered by the terms of an insurance contract, usually found on the first page of the policy. 
									<p></p>
									<b>FAMILY AUTO INSURANCE:</b> The automobile policy (most common in the industry) which provides protection for all members of the family.
									<p></p>
									<b>FINAL EXPENSES:</b>&nbsp; Expenses incurred at the time of a person&rsquo;s death.&nbsp; These include, but are not limited to:&nbsp; funeral costs, court expenses, current bills or debt, mortgages, loans and taxes.
									<p></p>
									<b>FIRST PARTY CLAIM:</b>&nbsp; A claim filed by an insured against his or her own insurance policy.
									<p></p>
									<b>FIXED BENEFIT:</b>&nbsp; A death benefit, the dollar amount of which does not vary.
									<p></p>
									<b>FLAT CANCELLATION:</b>&nbsp; The full cancellation of a policy as of the effective date of coverage, which requires the return of paid premium in full.
									<p></p>
									<b>FLOATER:</b>&nbsp; A type of insurance policy that covers property that is easily movable and provides additional coverage over what normal insurance policies do not. This can cover anything from jewelery to expensive stereo equipment.
									<p></p>
									<b>FLOOD EXCLUSION:</b>&nbsp; A provision in most all property insurance policies eliminating coverage for damage by flood and possibly other types of water damage, such as seepage and sewer backup.
									<p></p>
									<b>FRANCHISE INSURANCE:</b> Individual life or health insurance policies issued to a small group of people having a common affiliation or interest. Same as wholesale insurance.
									<p></p>
									<b>FREE LOOK:</b>&nbsp; Trial period required in most states where policy owners have up to 20 days to examine their new policies with no obligation.
									<p></p>
									<b>FULL COVERAGE:</b> Insurance which covers all losses, with no deductions, up to the amount of the insurance. 
									<p></p>
									
									
									<a name=g>
									<div class=glossarytitle> &nbsp;<b>G</b><a href=#home class=backtotop >back to top</a></div>
									<p></p>
									<b>GAP INSURANCE:</b>&nbsp; Insurance that pays the difference between the actual cash value of a vehicle and the amount still to be paid on the loan.&nbsp; Some gap policies may also cover the amount of the deductible.
									<p></p>
									<b>GARAGE LIABILITY INSURANCE:</b>&nbsp; Insurance coverage for the legal liability of automobile dealers, garages, repair shops and service stations for bodily injury and property damage arising out of their business operations.
									<p></p>
									<b>GENERAL LIABILITY:</b> A broad term meaning liability insurance, other than automobile, written to cover personal, professional and commercial risks. In respect to commercial liability, it protects business owners and operators from exposures such as liability arising from accidents resulting from the insured&rsquo;s premises or operations; products sold by the insured; operations completed by the insured, and contractual liability.
									<p></p>
									<b>GRACE PERIOD:</b>&nbsp; Period of time after the due date of a premium during which the policy remains in full force without penalty.
									<p></p>
									<b>GROSS NEGLIGENCE:</b>&nbsp; Willful and wanton misconduct.
									<p></p>
									<b>GROSS VEHICLE WEIGHT (GVW):</b>&nbsp; The weight specified by a manufacturer for the maximum total loaded weight of a single vehicle.
									<p></p>
									<b>GROUP OF COMPANIES:</b>&nbsp; Several insurance companies under common ownership and often common management.
									<p></p>
									
									
									<a name=h>
									<div class=glossarytitle> &nbsp;<b>H</b><a href=#home class=backtotop >back to top</a></div>
									<b>HAZARD:</b>&nbsp; A circumstance that increases the likelihood or probable severity of a loss.&nbsp; For example, the storing of explosives in a home basement is a hazard that increases the probability of an explosion.
									<p></p>
									<b>HEALTH MAINTENANCE ORGANIZATION (HMO):</b>&nbsp; Managed care plans that provides healthcare services to their members through networks of doctors, hospitals, and other healthcare providers.&nbsp; HMOs cover a wide variety of services, usually at a lower cost than traditional health care plans.
									<p></p>
									<b>HOLD HARMLESS AGREEMENT:</b>&nbsp; A contractual agreement that requires one contracting party to assume certain legal liabilities of the other party.
									<p></p>
									<b>HOMEOWNER&rsquo;S POLICY:</b> A policy in which Fire and extended or Broad coverage for dwelling and contents, residents&rsquo; theft insurance, additional living expense, and personal liability insurance are combined.
									<p></p>
									
									
									<a name=i>
									<div class=glossarytitle> &nbsp;<b>I</b><a href=#home class=backtotop >back to top</a></div>
									<p></p>
									<b>IMPAIRED RISKS:</b> In health insurance, individuals who can reasonably be expected to have an above-average number of claims due to medical history or physical impairment. Most impaired risks can be insured by use of a waiver or waiting period.
									<p></p>
									<b>INDEMNITY:</b>&nbsp; Restoration to the victim of a loss by payment, repair or replacement. 
									<p></p>
									<b>INDEPENDENT ADJUSTER:</b>&nbsp; A claims adjuster who provides adjustment services to insurance companies for a fee but is not employed by them.
									<p></p>
									<b>INSURABILITY:</b>&nbsp; The condition of the individual wishing to be insured, including his/her health, susceptibility to injury and life expectancy.
									<p></p>
									<b>INSURANCE POLICY:</b>&nbsp; The printed form which serves as the contract between an insurer and an insured.
									<p></p>
									<b>INSURED:</b>&nbsp; The person or organization covered by an insurance policy.
									<p></p>
									<b>INSURER:&nbsp;</b> The insurance company.&nbsp; The party that provides insurance coverage, typically through a contract of insurance.
									<p></p>
									<b>IRREVOCABLE BENEFICIARY:</b>&nbsp; A named beneficiary whose rights to life insurance policy proceeds are vested and cannot be canceled by the policy owner unless the beneficiary consents.
									<p></p>
									
									
									<a name=j>
									<div class=glossarytitle> &nbsp;<b>J</b><a href=#home class=backtotop >back to top</a></div>
									<p></p>
									<b>JUVENILE INSURANCE:</b> Life insurance policies written on the lives of children within specified age limits.
									<p></p>
									
									
									<a name=k>
									<div class=glossarytitle> &nbsp;<b>K</b><a href=#home class=backtotop >back to top</a></div>
									<p></p>
									<b>KEY MAN INSURANCE: </b>Insurance used for a business purpose, usually to reimburse a corporation for the loss it sustains when an important member of the firm dies.
