									<div class=glossarytitle> &nbsp;<b>Q</b><a href=#home class=backtotop >back to top</a></div>
									<p></p>
									<b>QUALIFYING EVENT:</b>&nbsp; An occurrence that triggers an insured&rsquo;s protection.
									<p></p>
									
									
									<a name=r>
									<div class=glossarytitle> &nbsp;<b>R</b><a href=#home class=backtotop >back to top</a></div>
									<p></p><b>RATE:</b> A charge per unit in determining insurance premiums.
									<p></p>
									<b>RATED POLICY:</b>&nbsp; A policy issued at a higher premium to cover a person classified as a greater-than-average risk, usually due to impaired health or a dangerous occupation.
									<p></p>
									<b>RATING TERRITORY:</b> A geographical grouping in which like hazards tend to equalize and permit the establishment of an equitable rate for the territory.
									<p></p>
									<b>RENTS OR RENTAL VALUE:</b> Insurance against loss of the rental value of a property; protects against loss of rents resulting from an insured peril.
									<p></p>
									<b>REAL PROPERTY:</b>&nbsp; Real estate including buildings and vegetation.
									<p></p>
									<b>RE-ENTRY OPTION:</b>&nbsp; An option in a renewable term life policy under which the policy owner is guaranteed, at the end of the term, to be able to renew his or her coverage without evidence of insurability, at a premium rate specified in the policy.
									<p></p>
									<b>REFUND:</b>&nbsp; An amount of money returned to the policyholder for overpayment of premium or if the policyholder is due unearned premium..
									<p></p>
									<b>REINSTATEMENT:</b>&nbsp; Putting a lapsed policy back in force by producing satisfactory evidence of insurability and paying any past-due premiums required.
									<p></p>
									<b>RENEWAL POLICY:</b>&nbsp; A policy issued to replace an expiring policy.
									<p></p>
									<b>RENTAL REIMBURSEMENT&nbsp; COVERAGE:</b>&nbsp; Optional auto insurance coverage that pays a set daily amount for a rental car if the policyholder&rsquo;s car is being repaired because of damage covered by the auto policy.
									<p></p>
									<b>RENTERS INSURANCE:</b>&nbsp; A form of property insurance that covers a policyholder&rsquo;s belongings against perils.&nbsp; It also provides personal liability coverage and additional living expenses.&nbsp; Possessions can be covered for their replacement cost or the actual cash value, which includes depreciation.
									<p></p>
									<b>REPLACEMENT COST:</b> The dollar amount needed to replace damaged personal property or dwelling property without deducting for depreciation but limited by the maximum dollaramount shown on the declarations page of the policy.
									<p></p>
									<b>RESIDUAL BENEFIT:</b>&nbsp; In disability insurance, a benefit paid when you suffer a loss of income due to covered disability or if loss of income persists.&nbsp; The benefit is based on a formula specified in your policy and it is generally a percentage of the full benefit.&nbsp; It may be paid up to the maximum benefit period.
									<p></p>
									<b>RETURN PREMIUM:</b>&nbsp; A portion of the premium returned to a policy owner as a result of cancellation, rate adjustment, or a calculation that an advance premium was in excess of the actual premium. 
									<p></p>
									<b>RIDER:</b> Endorsement. Special provision added to an original policy contract.
									<p></p>
									<b>RISK:</b> Chance of loss with respect to person, liability, or the property of the insured. Also used to mean "the insured."
									<p></p>
									
									
									<a name=s>
									<div class=glossarytitle> &nbsp;<b>S</b><a href=#home class=backtotop >back to top</a></div>
									<p></p>
									<b>SCHEDULE:</b> A list describing the property or items insured under the policy.
									<p></p>
									<b>SECONDARY BENEFICIARY:</b>&nbsp; An alternate beneficiary designated to receive payment, usually in the event the original beneficiary predeceases the insured.
									<p></p>
									<b>SHORT TERM CANCELLATION:</b>&nbsp; Cancellation of an insurance policy prior to the expiration date in which a penalty in the form of a less than full pro-rata premium refund is allowed.
									<p></p>
									<b>SINGLE INTEREST INSURANCE:</b>&nbsp; Insurance coverage for only one of the part ie shaving an insurable interest in that property.&nbsp; For instance, if a policy holder still owes money on his mortgage, and he does not have homeowners insurance, the lender may take out a single interest insurance policy to protect its own interest in the property.&nbsp; Single interest insurance protects only the policy owner, not the homeowner.
									<p></p>
									<b>SPECIAL CLASS:</b> Policies on which an extra premium rate is charged because an extra risk is presented.
									<p></p>
									<b>STANDARD PROVISIONS:</b> Policy provisions required by law or supervisory regulation &ndash;such as provisions relating to grace period and incontestability. 
									<p></p>
									<b>STANDARD RISK:</b> A person who, according to a life insurance company&rsquo;s underwriting standards, is entitled to insurance protection without extra rating or special restrictions.
									<p></p>
									<strong>STOCK COMPANY</strong>: A company organized and owned by stockholders, as distinguished from the mutual form of company which is owned by its policyholders.
<p></p>
									<b>SUBROGATION:</b>&nbsp; Assignment of rights of recovery from insured.
									<p></p>
									<b>SUBSTANDARD RISK:</b> A person who is considered an under-average or impaired insurance risk because of his physical condition, family or personal history of disease, occupation, residence in unhealthy climate, or dangerous habits.
									<p></p>
									<b>SURCHARGE:</b>&nbsp; An extra charge added to a premium by an insurance company.&nbsp; For automobile insurance, a surcharge is usually added if a policyholder borrows money on the policy or if the policy lapses for non-payment.
									<p></p>
									<b>SURRENDER CHARGES:</b> Charges that are deducted if a life insurance policy or annuity is cashed in (surrendered).&nbsp; These charges also are deducted if the policyholder borrows money on the policy or if the policy lapses for non-payment.
									<p></p>
									
									
									<a name=t>
									<div class=glossarytitle> &nbsp;<b>T</b><a href=#home class=backtotop >back to top</a></div>
									<p></p>
									<b>TERM:&nbsp; </b>The length of time for which a policy or bond is in force.
									<p></p>
									<b>TERMINATION:</b> The recording of a cancellation of an insurance policy. 
									<p></p>
									<b>TERM INSURANCE:</b> Life or health insurance protection during a limited number of years but expiring without value if the insured survives the stated period.
									<p></p>
									<b>THIRD PARTY CLAIM:</b> A claim filed against another person&rsquo;s insurance policy.
									<p></p>
									<b>TORT:</b>&nbsp; A private wrong or harm (other than a breach of contract) committed against another, resulting in legal liability.&nbsp; A tort is either intentional or accidental (negligent).&nbsp; Automobile liability insurance is purchased to protect one from suits arising from unintentional torts. 
									<p></p>
									<b>TOTAL DISABILITY:</b> Disability which prevents a person from performing (a) any of his occupational duties, or (b) any duties of any occupation, or ( c) any duties for which he is reasonably qualified. Definitions vary within policies.
									<p></p>
									<b>TOTAL LOSS:</b>&nbsp; A loss of sufficient size about which it can be said there is no value left.&nbsp; The complete destruction of the property.&nbsp; The term also is used to mean a loss requiring the maximum amount a policy will pay. 
									<p></p>
									<b>TOWING AND LABOR COVERAGE:</b>&nbsp; Auto insurance coverage that pays for towing charges when a car can&rsquo;t be driven.&nbsp; Also pays labor charges, such as changing a flat tire, at the place where the car broke down.
									<p></p>
									
									
									<a name=u>
									<div class=glossarytitle> &nbsp;<b>U</b><a href=#home class=backtotop >back to top</a></div>
									<p></p>
									<b>UNDERLYING COVERAGE:</b>&nbsp; The insurance or coverage in place on the same risk that will respond to loss before the excess policy is called on to pay any portion of the claim.
									<p></p>
									<b>UNDERWRITER:</b>&nbsp; The person trained in evaluating risks, deciding whether to accept or reject an application for a policy, and determining rates and coverages for risks.
									<p></p>
									<b>UNINSURED MOTORIST COVERAGE:</b> Protects the insured against financial loss resulting from bodily injury carelessly inflicted by an uninsured motorist, including a hit and run driver, who is legally liable. Bodily Injury Benefit. 
									<p></p>
									<b>UNEARNED PREMIUM:</b>&nbsp; The amount of a pre-paid premium that has not yet been used to buy coverage.&nbsp; For instance, if a policyholder paid in advance for a six-month premium, but then cancels the policy after two months, the company must refund the remaining four months of &ldquo;unearned&rdquo; premium.
									<p></p>
									<b>UNINSURED/UNDERINSURED MOTORIST (UM/UIM) COVERAGE:</b>&nbsp; Auto insurance coverage that pays for the policyholder&rsquo;s injuries and, in some states, property damage caused by a hit-and-run driver or a motorist without liability insurance.&nbsp; It will also pay when medical and car repair bills are higher than the other driver&rsquo;s liability coverage.
									<p></p>
									<b>UNIVERSAL LIFE INSURANCE:</b>&nbsp; An interest-sensitive life insurance policy with flexible, adjustable premium payments, that builds cash values.
									<p></p>
									<b>UNREPORTED CLAIMS:</b> Accidents which have occurred but which have not been reported or recorded.
									<p></p>
									<b>UMBRELLA POLICY:</b>&nbsp; Coverage for losses above the limit of an underlying policy or policies such as homeowners and auto insurance.
									<p></p>
									<b>USAGE:</b>&nbsp; Refers to the primary function or purpose in which you intend to operate your vehicle.&nbsp; For example, if you primarily drive your car to and from work, the usage is considered &ldquo;commute&rdquo;; if you are self-employed and you primarily drive to see customers, the usage is considered &ldquo;business.&rdquo;
									<p></p>
									<b>USUAL AND CUSTOMARY FEES:</b>&nbsp; Charges for medical services that refer to the amount approved by the carrier for payment.&nbsp; These charges may be based on rates usually charged by physicians and providers in your area; rate averages compiled by independent rating services; or rate averages compiled by the insurance company.
